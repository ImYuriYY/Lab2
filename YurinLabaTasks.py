def del_rep(array, result = []):
    #Обозначаем максимальное число по умолчанию 0
    maxCount = 0
    #Ссылаемся на список с результатом, в который будем добавлять числа
    resultArray = result
    #Обозначаем текущий индекс элемента как 0
    currentIndex = 0
    #Перебираем каждое число в списке пользователя
    for i in range(len(array)):
        #Если число в списке больше нашего максимального числа, то максимальное число будет равно числу из списка
        if(array[i] > maxCount):
            maxCount = array[i]
            #Сохраняем индекс максимального числа
            currentIndex = i
    #Если максимального числа нет в результатном списке и макс.число не равно 0, то добавляем его в результатный список
    if(not(maxCount in resultArray) and maxCount != 0):
        resultArray.insert(0,maxCount)
    #Если длинна списка пользователя не равна нулю, то:
    if(len(array) != 0 ):
        #Удаляем найденное максимальное число
        array.pop(currentIndex)
        #Снова вызываем функцию, пока длинна списка пользователя не станет равна 0
        del_rep(array, resultArray)
    #Когда длина списка пользователя равна 0 - выводим результатный список
    return(resultArray)


#Список пользователя
num = [1,2,1,2,3,1,2,3,4,65,31,7,4,2,5,1,2,3,4]

#Выводим результатный список в консоль
print(del_rep(num))





