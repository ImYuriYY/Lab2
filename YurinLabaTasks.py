def lim_max(turp, lim):
    #Создаем пустой список с максимальными числами из кортежа
    maxDigitsArray = []
    #Перебираем кортеж
    for i in range(len(turp)):
        #Проверяем каждое число кортежа
        if (turp[i] < lim):
            #Если оно меньше установленного лимита, то добавляем его в список с максимальными числами
            maxDigitsArray.append(turp[i])
    #Возвращаем максимальное значение из списка максимальных значений кортежа
    return(max(maxDigitsArray))

#Кортеж чисел
nums = (10,20,30,40,50,60,70,80,100)

#Лимит, установленный пользователем
limit = int(input("Введите лимит: "))

#Выводим результат
print(lim_max(nums, limit))






